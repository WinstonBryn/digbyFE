package digby.fe;

public class Contact {
	String contactName;
	String contactPhoneNumber;
	String contactBusinessNumber;
	String contactBusinessAddress;
	String contactHomeAddress;
	String contactCreditCard;

	public String getContactCreditCard() {
		return contactCreditCard;
	}

	public void setContactCreditCard(String contactCreditCard) {
		this.contactCreditCard = contactCreditCard;
	}

	public String getContactName() {
		return contactName;
	}

	public void setContactName(String contactName) {
		this.contactName = contactName;
	}

	public String getContactPhoneNumber() {
		return contactPhoneNumber;
	}

	public void setContactPhoneNumber(String contactPhoneNumber) {
		this.contactPhoneNumber = contactPhoneNumber;
	}

	public String getContactBusinessNumber() {
		return contactBusinessNumber;
	}

	public void setContactBusinessNumber(String contactBusinessNumber) {
		this.contactBusinessNumber = contactBusinessNumber;
	}

	public String getContactBusinessAddress() {
		return contactBusinessAddress;
	}

	public void setContactBusinessAddress(String contactBusinessAddress) {
		this.contactBusinessAddress = contactBusinessAddress;
	}

	public String getContactHomeAddress() {
		return contactHomeAddress;
	}

	public void setContactHomeAddress(String contactHomeAddress) {
		this.contactHomeAddress = contactHomeAddress;
	}

}
